const mockDriver = {
  /**
   * Generates a TxID.
   * @param {string} action - The event action name to generate the TxID for.
   * @returns A random transaction id for the given action.
   */
  _generateTxID: (action) => '' + Date.now() + '-r' + Math.floor(Math.random() * 1000) + '-' + (action || 'noAction'),
  /**
   * Function to call on event reception.
   */
  callback: null,

  /**
   * Init the fake driver.
   * @param responseHandler
   */
  init: function (responseHandler) {
    // Nothing to init.
    this.addCallback(responseHandler);
  },

  /**
   * Fake driver.
   * Adds callback to call on reception of event.
   * @param {Function} responseHandler - The function to call on reception of event.
   */
  addCallback: function (responseHandler) {
    this.callback = responseHandler;
  },

  /**
   * Mock the sending of the event.
   * @param {*} data
   */
  send: async function (data) {
    if (!data || !data.action) throw new Error('Missing data.action');
    if (data.action) data.TxID = this._generateTxID(data.action);
    if (!this.callback) throw new Error('No callback registered');
    if (!events[data.action]) {
      throw new Error('Unsupported event:' + data.action);
    }
    this.callback(events[data.action](data.data));
  },
};

export default mockDriver;
